num = int(input("Type a number from 1 to 7: "))

if num == 1:
    print("1. Sunday")
elif num == 2:
    print("2. Monday")
elif num == 3:
    print("3. Tuesday")
elif num == 4:
    print("4. Wednesday")
elif num == 5:
    print("5. Thursday")
elif num == 6:
    print("6. Friday")
elif num == 7:
    print("7. Saturday")
else:
    print("Invalid Entry!")
